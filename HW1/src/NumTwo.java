
public class NumTwo
{
	public static void main(String[] args)
	{

	    System.out.println("33/5 is " + 33/5);
	    //This returns an integer value because both numbers being divided are integers, therefore the answer will be truncated
	    
	    System.out.println("33/5.0 is " + 33/5.0);
	    //This returns a double value because one of the numbers is a double, so the final answer will be exact  
	}
}
